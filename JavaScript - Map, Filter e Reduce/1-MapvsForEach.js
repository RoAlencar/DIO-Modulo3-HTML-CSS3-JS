/**
 * O que é o Mapping function
 * - Cria um novo array
 * - Não modifica o original
 * - Realiza as operações em ordem
 */

//Usando map

const array = [1, 2, 3, 4, 5];

console.log(array.map((item) => item * 2));

//Usando forEach

const array2 = [1, 2, 3, 4, 5];

console.log(array.forEach((item) => item * 2))